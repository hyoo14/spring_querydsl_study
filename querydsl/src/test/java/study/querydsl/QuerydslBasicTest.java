    private List<Member> searchMember2(String usernameCond, Integer ageCond) {
        return queryFactory
                .selectFrom(member)
                .where(usernameEq(usernameCond), ageEq(ageCond)) //개발 때 메서드 안 보고 이것만 봄. 정리가 쫙 됨. 그래서 메서드 생겨도 이득.
//                .where(allEq(usernameCond, ageCond)) //이렇게 조립해서 작성할 수 있음!! 쿼리가 자바코드여서 합성할 수 있는 것! 널 처리가 귀찮긴 하지만 장점임. 특히 재사용성!
                .fetch();
    }

    private BooleanExpression usernameEq(String usernameCond) { //조립하려면 불린익스프레션 써야함 프리디케이트 말고!
        return usernameCond != null ? member.username.eq(usernameCond) : null;
    }

    private BooleanExpression ageEq(Integer ageCond) {
        return ageCond != null ? member.age.eq(ageCond) : null;
    }

    //광고 상태 isValid, 날짜가 IN: isServicable 예시를 보면 엄청난 장점임을 알 수 있었음(그냥 예시 들으심)
    //쿼리dsl 최대 장점!!

    private BooleanExpression allEq(String usernameCond, Integer ageCond){
        return usernameEq(usernameCond).and(ageEq(ageCond));
    }

